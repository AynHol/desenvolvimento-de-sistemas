export default class Calculadora {
    public somar(a: number, b: number) {
        var soma = a + b;
        return soma;
    }
    public subtrair(a: number, b: number) {
        var subtrair = a - b;
        return subtrair;
    }
    public multiplicar(a: number, b: number) {
        var multiplicar = a * b;
        return multiplicar;
    }
    public dividir(a: number, b: number) {
        var dividir = a / b;
        return dividir;
    }
}
